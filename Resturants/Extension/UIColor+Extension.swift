import Foundation
import UIKit

extension UIColor {
    
    static var ColorBlack         = UIColor(named: "ColorBlack") ?? #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    static var ColorWhite         = UIColor(named: "ColorWhite") ?? #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    static var ColorLightGreen    = UIColor(named: "ColorLightGreen") ?? #colorLiteral(red: 0.1176470588, green: 0.8470588235, blue: 0.3764705882, alpha: 1)
    static var ColorLightBlue     = UIColor(named: "ColorLightBlue") ?? #colorLiteral(red: 0.2549019608, green: 0.4431372549, blue: 0.5882352941, alpha: 1)
    static var ColorLightGray     = UIColor(named: "ColorLightGray") ?? #colorLiteral(red: 0.6352941176, green: 0.6352941176, blue: 0.6352941176, alpha: 1)
    static var ColorDarkGreen     = UIColor(named: "ColorDarkGreen") ?? #colorLiteral(red: 0.03529411765, green: 0.8078431373, blue: 0.5058823529, alpha: 1)
    static var ColorDarkBlue      = UIColor(named: "ColorDarkBlue") ?? #colorLiteral(red: 0.3607843137, green: 0.6431372549, blue: 0.9725490196, alpha: 1)
}

